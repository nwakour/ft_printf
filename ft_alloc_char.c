#include "ft_printf.h"

char	*ft_alloc_char(char **s, int i)
{
	if ((*s = (char*)malloc(i + 1)) == NULL)
	{
		g_len = -1;
		free(*s);
		return (NULL);
	}
	return (*s);
}
